export const getChapterForPage = (pageNumber: number): BookChapter | undefined => {
  return BOOK_CHAPTERS.find(
    ch => pageNumber >= ch.startPage && pageNumber <= ch.endPage
  );
};

export const getPageByNumber = (pageNumber: number): BookPage | undefined => {
  return BOOK_PAGES.find(p => p.pageNumber === pageNumber);
};
